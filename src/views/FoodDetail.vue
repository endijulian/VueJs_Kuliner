<script>
import Navbar from "@/components/Navbar.vue";
import axios from "axios";

export default {
  name: "FoodDetail",
  components: {
    Navbar,
  },
  data() {
    return {
      products: [],
    };
  },
  methods: {
    setProduct(data) {
      this.products = data;
    },
  },
  mounted() {
    axios
      .get("http://localhost:3000/products" + this.$route.params.id)
      // handle success
      .then((response) => this.setProduct(response.data))
      // handle error
      .catch((error) => console.log("Gagal : ", error));
  },
};
